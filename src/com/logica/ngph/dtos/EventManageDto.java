package com.logica.ngph.dtos;

import java.io.Serializable;

public class EventManageDto implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String eventID;
	private String eventDesc;
	private Integer alertable;
	private String alert_For;
	private String alert_To;
	private String alert_type;
	private String alert_consolidate;
	
	public Integer getAlertable() {
		return alertable;
	}
	public void setAlertable(Integer alertable) {
		this.alertable = alertable;
	}
	public String getAlert_For() {
		return alert_For;
	}
	public void setAlert_For(String alert_For) {
		this.alert_For = alert_For;
	}
	public String getAlert_To() {
		return alert_To;
	}
	public void setAlert_To(String alert_To) {
		this.alert_To = alert_To;
	}
	public String getAlert_type() {
		return alert_type;
	}
	public void setAlert_type(String alert_type) {
		this.alert_type = alert_type;
	}
	public String getAlert_consolidate() {
		return alert_consolidate;
	}
	public void setAlert_consolidate(String alert_consolidate) {
		this.alert_consolidate = alert_consolidate;
	}
	public String getEventID() {
		return eventID;
	}
	public void setEventID(String eventID) {
		this.eventID = eventID;
	}
	public String getEventDesc() {
		return eventDesc;
	}
	public void setEventDesc(String eventDesc) {
		this.eventDesc = eventDesc;
	}
	

}
