package com.logica.ngph.dtos;

import java.io.Serializable;


public class FileUploadFieldDetail implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 3505983669588662219L;
	private String tableName;
	private String columnName;
	private String check;
	private int filedNumber;
	private String algorithm;
	private int fieldLength;
	

	public int getFieldLength() {
		return fieldLength;
	}
	public void setFieldLength(int fieldLength) {
		this.fieldLength = fieldLength;
	}
	public int getFiledNumber() {
		return filedNumber;
	}
	public void setFiledNumber(int filedNumber) {
		this.filedNumber = filedNumber;
	}
	public String getAlgorithm() {
		return algorithm;
	}
	public void setAlgorithm(String algorithm) {
		this.algorithm = algorithm;
	}
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	public String getCheck() {
		return check;
	}
	public void setCheck(String check) {
		this.check = check;
	}
	
}
